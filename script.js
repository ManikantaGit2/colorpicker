let mainContainerElement = document.getElementById("mainContainer");
let hexCodeElement = document.getElementById("hexCode");


function onChangeGreen() {
    mainContainerElement.style.backgroundColor = "Green",
    hexCodeElement.textContent ="Green"
}
function onChangeYellow() {
    mainContainerElement.style.backgroundColor = "Yellow",
    hexCodeElement.textContent ="Yellow"
}
function onChangeViolet() {
    mainContainerElement.style.backgroundColor = "Violet",
    hexCodeElement.textContent ="Violet"
}
function onChangeAqua() {
    mainContainerElement.style.backgroundColor = "Aqua",
    hexCodeElement.textContent ="Aqua"
}